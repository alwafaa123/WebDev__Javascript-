let angka1 = 5;
let angka2 = 5;

let hasilTambah = angka1 + angka2; // 13
console.log("Penambahan:", hasilTambah);

let hasilKurang = angka1 - angka2; // 7
console.log("Pengurangan:", hasilKurang);

let hasilKali = angka1 * angka2; // 30
console.log("Perkalian:", hasilKali);

let hasilBagi = angka1 / angka2; // 3.333...
console.log("Pembagian:", hasilBagi);

let sisaBagi = angka1 % angka2; // 1 (karena 10 dibagi 3 adalah 3 sisa 1)
console.log("Sisa Bagi (Modulus):", sisaBagi);

let hasilPangkat = angka2 ** angka1; // 3 pangkat 10
console.log("Pangkat:", hasilPangkat);

// Operator tambah bisa juga untuk menggabungkan string (concatenation)
let teks1 = "Halo";
let teks2 = "Dunia";
let gabungTeks = teks1 + " " + teks2; // "Halo Dunia"
console.log("Gabung Teks:", gabungTeks);