// script.js

let total = 100;
console.log("Total awal:", total); // 100

total += 50; // total = total + 50;  => total sekarang 150
console.log("Total setelah ditambah:", total); // 150

total -= 20; // total = total - 20;  => total sekarang 130
console.log("Total setelah dikurangi:", total); // 130

let poin = 5;
poin *= 2; // poin = poin * 2; => poin sekarang 10
console.log("Poin setelah dikali:", poin); // 10