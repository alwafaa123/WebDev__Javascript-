// operator aritmatika jika ke duanya adalah angka

//  expresi dan statement +-/*
// % <-- modulo
// ** <-- pangkat atau power
// ++ <-- incremet
// -- <--decrement

let x = 10;
console.log(x + 1.5);
console.log(x / 1);
console.log(x * 1);
console.log(x - 1);

// aritmatika khusus di javscript
console.log(x % 4); // sisa pembagian
console.log(x ** 2); // dipangkatkan

// pemograman biasa
x = x + 1;
console.log(x);
x = x + 1;
console.log(x);
x++;
x++;
console.log(x);

x = x - 1;
console.log(x);

x--;
console.log(x);

